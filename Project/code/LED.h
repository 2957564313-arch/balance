#ifndef __LED_H__
#define __LED_H__

#include "zf_common_headfile.h"

// ================================== 对外宏定义 ==================================
#define LED_PIN     IO_P52                              // LED 控制引脚（P5.2，低电平点亮）

// ================================== 对外 API 声明 ==================================

//--------------------------------------------------------------------------------------------------
// 函数简介        LED 引脚初始化
// 参数说明        void
// 返回参数        void
// 使用示例        LED_Init();
// 备注信息        配置为推挽输出，上电默认高电平（LED 熄灭）
//--------------------------------------------------------------------------------------------------
void LED_Init(void);

//--------------------------------------------------------------------------------------------------
// 函数简介        点亮 LED
// 参数说明        void
// 返回参数        void
// 使用示例        LED_ON();
// 备注信息        输出低电平点亮 LED
//--------------------------------------------------------------------------------------------------
void LED_ON(void);

//--------------------------------------------------------------------------------------------------
// 函数简介        关闭 LED
// 参数说明        void
// 返回参数        void
// 使用示例        LED_OFF();
// 备注信息        输出高电平熄灭 LED
//--------------------------------------------------------------------------------------------------
void LED_OFF(void);

#endif
