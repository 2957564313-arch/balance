#ifndef __MODE_H__
#define __MODE_H__

#include "zf_common_headfile.h"

// ================================== 对外类型定义 ==================================
// 启动模式定义（1~5）
typedef enum
{
    MODE_1_BALANCE = 1,                             // 纯平衡
    MODE_2_TRACK_1,                                 // 跑道模式1（直道+半圆）
    MODE_3_TRACK_2,                                 // 跑道模式2（对角+半圆 4 圈）
    MODE_4_RECORD_PLAY,                             // 直线录制回放
    MODE_5_REMOTE                                   // 蓝牙遥控
} start_mode_enum;

// ================================== 对外变量声明 ==================================
// 模式公共变量
extern uint8 current_mode;                          // 当前运行模式
extern int16 target_speed_val;                      // Mode2/3 速度目标（counts/20ms）
extern int16 target_turn_val;                       // Mode2/3 转向目标
extern volatile uint8 g_speed_pid_reset_req;        // Mode2/3 段切换请求清速度环

// ================================== 对外 API 声明（Mode2/3） ==================================

//--------------------------------------------------------------------------------------------------
// 函数简介        模式模块初始化
// 参数说明        void
// 返回参数        void
// 使用示例        Mode_Init();
// 备注信息        清零模式公共变量与内部状态
//--------------------------------------------------------------------------------------------------
void Mode_Init(void);

//--------------------------------------------------------------------------------------------------
// 函数简介        启动指定模式
// 参数说明        mode             模式编号（MODE_2_TRACK_1 / MODE_3_TRACK_2）
// 返回参数        void
// 使用示例        Mode_Start(MODE_2_TRACK_1);
// 备注信息        构建路线并进入陀螺标定阶段
//--------------------------------------------------------------------------------------------------
void Mode_Start(uint8 mode);

//--------------------------------------------------------------------------------------------------
// 函数简介        停止当前模式
// 参数说明        void
// 返回参数        void
// 使用示例        Mode_Stop();
// 备注信息        清零输出并标记停止
//--------------------------------------------------------------------------------------------------
void Mode_Stop(void);

//--------------------------------------------------------------------------------------------------
// 函数简介        Mode2/3 20ms 控制任务
// 参数说明        void
// 返回参数        void
// 使用示例        Mode_Task_20ms();
// 备注信息        读取灰度与陀螺数据，输出速度与转向目标
//--------------------------------------------------------------------------------------------------
void Mode_Task_20ms(void);

//--------------------------------------------------------------------------------------------------
// 函数简介        路径按键处理
// 参数说明        void
// 返回参数        void
// 使用示例        Mode_Path_Key_Handler();
// 备注信息        处理路径相关按键事件
//--------------------------------------------------------------------------------------------------
void Mode_Path_Key_Handler(void);

// ================================== 对外 API 声明（Mode4） ==================================

//--------------------------------------------------------------------------------------------------
// 函数简介        Mode4 初始化
// 参数说明        void
// 返回参数        void
// 使用示例        Mode4_Init();
// 备注信息        清零状态并尝试从 Flash 加载录制数据
//--------------------------------------------------------------------------------------------------
void  Mode4_Init(void);

//--------------------------------------------------------------------------------------------------
// 函数简介        进入 Mode4 界面
// 参数说明        void
// 返回参数        void
// 使用示例        Mode4_Enter();
// 备注信息        激活 Mode4 并显示初始 UI
//--------------------------------------------------------------------------------------------------
void  Mode4_Enter(void);

//--------------------------------------------------------------------------------------------------
// 函数简介        查询 Mode4 是否处于激活状态
// 参数说明        void
// 返回参数        uint8            1=激活 0=未激活
// 使用示例        if (Mode4_IsActive()) { ... }
// 备注信息        供主流程判断是否由 Mode4 接管界面与控制
//--------------------------------------------------------------------------------------------------
uint8 Mode4_IsActive(void);

//--------------------------------------------------------------------------------------------------
// 函数简介        Mode4 5ms 固定时基任务
// 参数说明        void
// 返回参数        void
// 使用示例        Mode4_Tick_5ms();
// 备注信息        执行陀螺积分、偏置标定与计时，建议在中断中调用
//--------------------------------------------------------------------------------------------------
void  Mode4_Tick_5ms(void);

//--------------------------------------------------------------------------------------------------
// 函数简介        Mode4 主循环 5ms 任务
// 参数说明        void
// 返回参数        void
// 使用示例        Mode4_Task_5ms();
// 备注信息        仅处理 UI 刷新、按键与 Flash 保存
//--------------------------------------------------------------------------------------------------
void  Mode4_Task_5ms(void);

//--------------------------------------------------------------------------------------------------
// 函数简介        Mode4 20ms 控制任务
// 参数说明        spd_cmd_out              输出参数 速度环目标指针
// 参数说明        turn_cmd_out             输出参数 转向命令指针
// 参数说明        speed_pid_enable_out     输出参数 速度环使能指针
// 返回参数        void
// 使用示例        Mode4_Task_20ms(&spd, &turn, &en);
// 备注信息        录制时关闭速度环，回放时输出匀速与航向保持
//--------------------------------------------------------------------------------------------------
void  Mode4_Task_20ms(int16 *spd_cmd_out, int16 *turn_cmd_out, uint8 *speed_pid_enable_out);

#endif
