#include "Buzzer.h"

// ================================== 内部状态变量 ==================================
static uint16 beep_timer = 0;                          // 5ms 节拍计数器（0 表示静音）

// ================================== 对外 API 函数 ==================================

//--------------------------------------------------------------------------------------------------
// 函数简介        蜂鸣器初始化
// 参数说明        void
// 返回参数        void
// 使用示例        Buzzer_Init();
// 备注信息        上电默认关闭，低电平表示静音
//--------------------------------------------------------------------------------------------------
void Buzzer_Init(void)
{
    gpio_init(BEEP_PIN, GPO, 0, GPO_PUSH_PULL);        // 上电默认关闭，低电平表示静音
    beep_timer = 0;
}

//--------------------------------------------------------------------------------------------------
// 函数简介        开启蜂鸣器并设置鸣叫时长
// 参数说明        ms               鸣叫持续时间（毫秒），最小值为 5ms
// 返回参数        void
// 使用示例        Buzzer_Beep(200);
// 备注信息        调用后蜂鸣器立即鸣叫，需配合 Buzzer_Task 周期调用完成自动关闭
//--------------------------------------------------------------------------------------------------
void Buzzer_Beep(uint16 ms)
{
    if (ms < 5) ms = 5;
    beep_timer = (uint16)(ms / 5u);

    gpio_set_level(BEEP_PIN, 1);                        // 开始鸣叫，高电平表示开启
}

//--------------------------------------------------------------------------------------------------
// 函数简介        蜂鸣器 5ms 定时任务
// 参数说明        void
// 返回参数        void
// 使用示例        Buzzer_Task();
// 备注信息        计时到后自动关闭蜂鸣器
//--------------------------------------------------------------------------------------------------
void Buzzer_Task(void)
{
    if (beep_timer > 0u)
    {
        beep_timer--;
        if (beep_timer == 0u)
        {
            gpio_set_level(BEEP_PIN, 0);                // 计时结束，关闭蜂鸣器，低电平表示关闭
        }
    }
}