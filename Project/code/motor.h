#ifndef __MOTOR_H__
#define __MOTOR_H__

#include "zf_common_headfile.h"

// ================================== 对外宏定义 ==================================
// PWM 参数
#define PWM_FREQ                (17000u)     // 17kHz
#define MOTOR_MAX_DUTY          (10000)      // 对应 PWM_DUTY_MAX=10000

// 电机方向：若方向定义与实物不一致，可调整符号
#define MOTOR_L_DIR             (-1)
#define MOTOR_R_DIR             (1)

// TB6612 引脚定义
#define MOTOR_L_EN_PWM          (PWMA_CH1P_P60)   // P6.0 -> TB6612 PWMA
#define MOTOR_R_EN_PWM          (PWMA_CH2P_P62)   // P6.2 -> TB6612 PWMB

#define MOTOR_L_IN1_IO          (IO_P64)
#define MOTOR_L_IN2_IO          (IO_P66)
#define MOTOR_R_IN1_IO          (IO_P63)
#define MOTOR_R_IN2_IO          (IO_P65)

// 编码器：仅使用左轮硬件正交（PWMB）
// 左编码器 A/B：P2.0/P2.1
// 说明：PWMB 进入 Encoder Mode 后，PWMB 这组不能再当 PWM 输出使用
#define ENCODER_L_A_IO          (IO_P20)
#define ENCODER_L_B_IO          (IO_P21)

// 方向翻转：如“向前推计数为负”，将该宏改为 1
#define MOTOR_ENC_L_INV         (1)

// ================================== 对外变量声明 ==================================
extern volatile uint8 run_flag;                     // 1=允许输出电机
extern volatile int16 car_speed;                    // 当前速度（counts/20ms）
extern volatile int32 car_pos_cnt;                  // 位置累计（counts）
extern volatile short int left_motor_encoder_data;  // 左轮 20ms 增量
extern volatile short int right_motor_encoder_data; // 兼容旧逻辑，镜像等于 left

extern volatile int16 motor_turn_out;               // 转向差动输出

extern int16 left_motor_duty;                       // 左电机占空比命令
extern int16 right_motor_duty;                      // 右电机占空比命令

// ================================== 对外 API 声明 ==================================

//--------------------------------------------------------------------------------------------------
// 函数简介        电机模块初始化
// 参数说明        void
// 返回参数        void
// 使用示例        Motor_Init();
// 备注信息        初始化 PWM/编码器/控制状态
//--------------------------------------------------------------------------------------------------
void  Motor_Init(void);

//--------------------------------------------------------------------------------------------------
// 函数简介        电机停止并复位状态
// 参数说明        void
// 返回参数        void
// 使用示例        Motor_Stop();
// 备注信息
//--------------------------------------------------------------------------------------------------
void  Motor_Stop(void);

//--------------------------------------------------------------------------------------------------
// 函数简介        设置左电机占空比
// 参数说明        duty             带符号占空比命令
// 返回参数        void
// 使用示例        Motor_Set_L(1000);
// 备注信息
//--------------------------------------------------------------------------------------------------
void  Motor_Set_L(int16 duty);

//--------------------------------------------------------------------------------------------------
// 函数简介        设置右电机占空比
// 参数说明        duty             带符号占空比命令
// 返回参数        void
// 使用示例        Motor_Set_R(1000);
// 备注信息
//--------------------------------------------------------------------------------------------------
void  Motor_Set_R(int16 duty);

//--------------------------------------------------------------------------------------------------
// 函数简介        动态电机控制输出
// 参数说明        void
// 返回参数        void
// 使用示例        dynamic_motor_control();
// 备注信息        融合平衡输出与转向输出
//--------------------------------------------------------------------------------------------------
void  dynamic_motor_control(void);

//--------------------------------------------------------------------------------------------------
// 函数简介        1ms 编码器采样任务
// 参数说明        void
// 返回参数        void
// 使用示例        Motor_Encoder_Tick_1ms();
// 备注信息        建议在 1ms 定时中断中调用
//--------------------------------------------------------------------------------------------------
void  Motor_Encoder_Tick_1ms(void);

//--------------------------------------------------------------------------------------------------
// 函数简介        20ms 转向命令映射（旧接口）
// 参数说明        turn_cmd_raw     原始转向命令
// 返回参数        void
// 使用示例        Motor_Turn_Tick_20ms(turn_cmd);
// 备注信息
//--------------------------------------------------------------------------------------------------
void  Motor_Turn_Tick_20ms(int16 turn_cmd_raw);

//--------------------------------------------------------------------------------------------------
// 函数简介        复位转向环状态
// 参数说明        void
// 返回参数        void
// 使用示例        Motor_Turn_Reset();
// 备注信息
//--------------------------------------------------------------------------------------------------
void  Motor_Turn_Reset(void);

//--------------------------------------------------------------------------------------------------
// 函数简介        5ms 转向控制计算（新接口）
// 参数说明        turn_err         转向误差
// 参数说明        gyro_z           Z 轴角速度
// 返回参数        void
// 使用示例        Motor_Turn_Tick_5ms(err, gyro_z);
// 备注信息
//--------------------------------------------------------------------------------------------------
void  Motor_Turn_Tick_5ms(int16 turn_err, int16 gyro_z);

#endif
