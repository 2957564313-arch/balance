#ifndef __BUZZER_H__
#define __BUZZER_H__

#include "zf_common_headfile.h"

// ================================== 对外宏定义 ==================================
#define BEEP_PIN  IO_P67                                // 蜂鸣器控制引脚（P6.7）

// ================================== 对外 API 声明 ==================================

//--------------------------------------------------------------------------------------------------
// 函数简介        蜂鸣器初始化
// 参数说明        void
// 返回参数        void
// 使用示例        Buzzer_Init();
// 备注信息        上电默认关闭，低电平表示静音
//--------------------------------------------------------------------------------------------------
void Buzzer_Init(void);

//--------------------------------------------------------------------------------------------------
// 函数简介        开启蜂鸣器并设置鸣叫时长
// 参数说明        ms               鸣叫持续时间（毫秒），最小值为 5ms
// 返回参数        void
// 使用示例        Buzzer_Beep(200);
// 备注信息        调用后蜂鸣器立即鸣叫，需配合 Buzzer_Task 周期调用完成自动关闭
//--------------------------------------------------------------------------------------------------
void Buzzer_Beep(uint16 ms);

//--------------------------------------------------------------------------------------------------
// 函数简介        蜂鸣器 5ms 定时任务
// 参数说明        void
// 返回参数        void
// 使用示例        Buzzer_Task();
// 备注信息        计时到后自动关闭蜂鸣器
//--------------------------------------------------------------------------------------------------
void Buzzer_Task(void);

#endif