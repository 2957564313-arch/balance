#ifndef __KEY_H__
#define __KEY_H__

#include "zf_common_headfile.h"

// ================================== 对外宏定义 ==================================
#define KEY1_PIN    IO_P70                              // 上键引脚
#define KEY2_PIN    IO_P71                              // 下键引脚
#define KEY3_PIN    IO_P72                              // 确认键引脚
#define KEY4_PIN    IO_P73                              // 返回键引脚

#define KEY_NAME_UP         0                           // 上键编号
#define KEY_NAME_DOWN       1                           // 下键编号
#define KEY_NAME_CONFIRM    2                           // 确认键编号
#define KEY_NAME_BACK       3                           // 返回键编号
#define KEY_COUNT           4                           // 按键总数

#define KEY_DOWN    0x01                                // 按下瞬间（下降沿 一次性）
#define KEY_UP      0x02                                // 抬起瞬间（上升沿 一次性）
#define KEY_HOLD    0x04                                // 正在按住（持续有效 不自动清除）
#define KEY_SINGLE  0x08                                // 单击（松手后触发 < 1秒）
#define KEY_LONG    0x10                                // 长按（按住 >= 1秒时触发一次）
#define KEY_REPEAT  0x20                                // 长按连发（长按后每 200ms 触发一次）

// ================================== 对外 API 声明 ==================================

//--------------------------------------------------------------------------------------------------
// 函数简介        按键引脚初始化
// 参数说明        void
// 返回参数        void
// 使用示例        Key_Init();
// 备注信息        配置按键引脚为输入上拉模式，低电平有效
//--------------------------------------------------------------------------------------------------
void  Key_Init(void);

//--------------------------------------------------------------------------------------------------
// 函数简介        按键扫描状态机驱动
// 参数说明        void
// 返回参数        void
// 使用示例        Key_Tick();
// 备注信息        内置消抖，支持单击、长按与连发检测，无需额外延时
//--------------------------------------------------------------------------------------------------
void  Key_Tick(void);

//--------------------------------------------------------------------------------------------------
// 函数简介        查询按键事件
// 参数说明        n                按键编号 (0~KEY_COUNT-1)
// 参数说明        Flag             需要查询的事件标志（如 KEY_SINGLE、KEY_LONG）
// 返回参数        uint8            1=事件发生 0=无事件
// 使用示例        Key_Check(KEY_NAME_UP, KEY_SINGLE);
// 备注信息        除 KEY_HOLD 外，事件读取后自动清除（一次性消费）
//--------------------------------------------------------------------------------------------------
uint8 Key_Check(uint8 n, uint8 Flag);

//--------------------------------------------------------------------------------------------------
// 函数简介        简易单击检测
// 参数说明        void
// 返回参数        uint8            发生单击的按键号 (1~4) 无事件返回 0
// 使用示例        uint8 key = Key_Check_Simple();
// 备注信息        轮询全部按键，返回第一个发生单击事件的按键号
//--------------------------------------------------------------------------------------------------
uint8 Key_Check_Simple(void);

#endif