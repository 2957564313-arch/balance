#include "LED.h"

// ================================== 对外 API 函数 ==================================

//--------------------------------------------------------------------------------------------------
// 函数简介        LED 引脚初始化
// 参数说明        void
// 返回参数        void
// 使用示例        LED_Init();
// 备注信息        配置为推挽输出，上电默认高电平（LED 熄灭）
//--------------------------------------------------------------------------------------------------
void LED_Init(void)
{
    gpio_init(LED_PIN, GPO, 1, GPO_PUSH_PULL);          // 推挽输出，默认高电平，LED 熄灭
}

//--------------------------------------------------------------------------------------------------
// 函数简介        点亮 LED
// 参数说明        void
// 返回参数        void
// 使用示例        LED_ON();
// 备注信息        输出低电平点亮 LED
//--------------------------------------------------------------------------------------------------
void LED_ON(void)
{
    gpio_set_level(LED_PIN, 0);                         // 低电平点亮
}

//--------------------------------------------------------------------------------------------------
// 函数简介        关闭 LED
// 参数说明        void
// 返回参数        void
// 使用示例        LED_OFF();
// 备注信息        输出高电平熄灭 LED
//--------------------------------------------------------------------------------------------------
void LED_OFF(void)
{
    gpio_set_level(LED_PIN, 1);                         // 高电平熄灭
}
