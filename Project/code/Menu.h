#ifndef __MENU_H__
#define __MENU_H__

#include "zf_common_headfile.h"

// ================================== 对外类型定义 ==================================
//
// 菜单页：参数 + SAVE
//  - MODE
//  - 角速度环：P I D
//  - 角度环：P I D
//  - 速度环：P I D
//  - 转向PPDD：Kp Kp2 Kd Kd2
//  - 零点
//  - SAVE
//
typedef enum
{
    MENU_ITEM_MODE = 0,         // 启动模式

    MENU_ITEM_RATE_KP,          // 角速度环 Kp
    MENU_ITEM_RATE_KI,          // 角速度环 Ki
    MENU_ITEM_RATE_KD,          // 角速度环 Kd

    MENU_ITEM_ANGLE_KP,         // 角度环 Kp
    MENU_ITEM_ANGLE_KI,         // 角度环 Ki
    MENU_ITEM_ANGLE_KD,         // 角度环 Kd

    MENU_ITEM_SPEED_KP,         // 速度环 Kp
    MENU_ITEM_SPEED_KI,         // 速度环 Ki
    MENU_ITEM_SPEED_KD,         // 速度环 Kd

    MENU_ITEM_TURN_KP,          // 转向 Kp
    MENU_ITEM_TURN_KP2,         // 转向 Kp2
    MENU_ITEM_TURN_KD,          // 转向 Kd
    MENU_ITEM_TURN_KD2,         // 转向 Kd2

    MENU_ITEM_MECH_ZERO,        // 机械零点

    MENU_ITEM_SAVE,             // 保存参数

    MENU_ITEM_MAX               // 菜单项总数（哨兵值）

} MenuItem_e;

// ================================== 对外 API 声明 ==================================
//--------------------------------------------------------------------------------
// 函数简介      菜单模块初始化
// 参数说明      void
// 返回参数      void
// 使用示例      Menu_Init();
// 备注信息      上电后初始化菜单模块，进入主页面，并将 run_flag 清零
//--------------------------------------------------------------------------------
void Menu_Init(void);

//--------------------------------------------------------------------------------
// 函数简介      进入菜单页面
// 参数说明      void
// 返回参数      void
// 使用示例      Menu_Enter();
// 备注信息      进入菜单页面，同时关闭电机输出
//--------------------------------------------------------------------------------
void Menu_Enter(void);

//--------------------------------------------------------------------------------
// 函数简介      退出菜单页面
// 参数说明      void
// 返回参数      void
// 使用示例      Menu_Exit();
// 备注信息      退出菜单并回到主页面，同时关闭电机输出
//--------------------------------------------------------------------------------
void Menu_Exit(void);

//--------------------------------------------------------------------------------
// 函数简介      菜单周期任务
// 参数说明      void
// 返回参数      uint8           0=当前在菜单页  1=当前在主页面
// 使用示例      uint8 page = Menu_Task();
// 备注信息      UI 周期任务：主页面 OK=RUN/BK=MENU；菜单页 UP/DN 翻页、OK 编辑或保存、BK 返回
//--------------------------------------------------------------------------------
uint8 Menu_Task(void);

#endif
